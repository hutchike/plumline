<p>
   Plumline is not a replacement for<br/>
   face-to-face Sangha, but a viable<br/>
   alternative for those who cannot <br/>
   regularly attend traditional Sangha.
</p>

<p>
   The Plumline website is a support<br/>
   center for those wishing to<br/>
   establish, build and maintain an<br/> 
   online Sangha. A place to share<br/>
   ideas and resources as online<br/>
   Sangha develops.
</p>
