<p>
Plumline supports the establishment, building and maintainence of online Sanghas in the Thich Nhat Hanh tradition.  It coordinates contact between online sanghas and potential members. It also provides a set of links to <a href="/practice/links">organizations and monasteries</a> of the Thich Nhat Hanh tradition to help visitors find information about texts, talks and retreats.
</p>
<p>
The online sanghas currently represented by this website are in English.
</p>
<p>
There are two types of online sanghas represented.  One of the first sanghas in Plumline still uses Yahoo's instant text conferencing.  The more recent sanghas use Google+ audio/video conferencing.  Both types of meetings are live meetings held in real-time, just using different technical means to communicate.
</p>
<p>
To learn about the format of our sanghas, please read our <a href="practice/mindfulness">Mindfulness Practice</a> page.
</p>
